package facade_singleton.classes;

public class TheaterLights {

    public void on(){
        System.out.println("Ligando as luzes do ambiente.");
    }

    public void off(){
        System.out.println("Desligando as luzes do ambiente.");
    }
    public void dim(){
        System.out.println("Diminuindo as luzes do ambiente para 10%.");
    }

}
